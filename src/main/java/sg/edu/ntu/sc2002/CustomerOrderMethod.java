package sg.edu.ntu.sc2002;

/** List of methods related to customer order actions */
public enum CustomerOrderMethod {
    /** Add item to cart */
    ADD_TO_CART,
    /** Remove item from cart */
    REMOVE_FROM_CART,
    /** Customise item in cart */
    CUSTOMISE_ITEM_IN_CART,
    /** View items in cart */
    VIEW_CART,
    /** Pay for the order */
    PAY
}
